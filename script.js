const provider = new ethers.providers.Web3Provider(window.ethereum);
let signer;
let contract;

provider.send("eth_requestAccounts", []).then(() => {
  provider.listAccounts().then((accounts) => {
    signer = provider.getSigner(accounts[0]);
    contract = new ethers.Contract(contractAddress, abi, signer);
    const eventName = "Send";
    const eventFilter = contract.filters[eventName]();
    console.log(eventFilter);
    contract.on(eventFilter, (sender, receiver, amount, event) => {
      console.log(
        "Sender: ",
        sender,
        "Receiver: ",
        receiver,
        "Amount: ",
        amount
      );
    });
  });
});

async function mintCoins() {
  const amount = document.querySelector("#amount").value;
  await contract.mint(parseInt(amount));
  console.log("Credit Address: ", await signer.getAddress());
}

async function getBalance() {
  const addr = document.querySelector("#addr").value;
  console.log(addr);
  const balance = await contract.getBalance(addr);
  document.querySelector("#avail_bal").innerText = balance;
}

async function sendCoins() {
  const sendersAddress = await signer.getAddress();
  const addr = document.querySelector("#addr").value;
  const amount = document.querySelector("#amount").value;
  await contract.send(sendersAddress, addr, parseInt(amount));
  console.log("Sent successfully.", sendersAddress, addr);
}
